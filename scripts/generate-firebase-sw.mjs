import fs from "node:fs";
import path from "node:path";
import { loadEnv } from "vite";

const mode = process.argv[2] || "development";
const rootDir = process.cwd();
const env = loadEnv(mode, rootDir, "");
const outputPath = path.join(rootDir, "public", "firebase-messaging-sw.js");

const requiredConfig = {
  apiKey: env.VITE_FIREBASE_API_KEY,
  authDomain: env.VITE_FIREBASE_AUTH_DOMAIN,
  projectId: env.VITE_FIREBASE_PROJECT_ID,
  storageBucket: env.VITE_FIREBASE_STORAGE_BUCKET,
  messagingSenderId: env.VITE_FIREBASE_MESSAGING_SENDER_ID,
  appId: env.VITE_FIREBASE_APP_ID,
};

const missingKeys = Object.entries(requiredConfig)
  .filter(([, value]) => !value)
  .map(([key]) => key);

const disabledWorkerSource = `// Auto-generated by scripts/generate-firebase-sw.mjs
// Firebase messaging is disabled until the required VITE_FIREBASE_* env vars are set.
self.addEventListener("install", () => self.skipWaiting());
self.addEventListener("activate", (event) => event.waitUntil(self.clients.claim()));
`;

if (missingKeys.length > 0) {
  fs.writeFileSync(outputPath, disabledWorkerSource, "utf8");
  console.log(
    `Generated fallback firebase-messaging-sw.js because config is incomplete: ${missingKeys.join(", ")}`,
  );
  process.exit(0);
}

const firebaseConfigLiteral = JSON.stringify(requiredConfig, null, 2);

const workerSource = `// Auto-generated by scripts/generate-firebase-sw.mjs
importScripts("https://www.gstatic.com/firebasejs/11.4.0/firebase-app-compat.js");
importScripts("https://www.gstatic.com/firebasejs/11.4.0/firebase-messaging-compat.js");

firebase.initializeApp(${firebaseConfigLiteral});

const messaging = firebase.messaging();

messaging.onBackgroundMessage((payload) => {
  const title = payload.notification?.title || "Task Reminder";
  const body = payload.notification?.body || "You have a task deadline approaching.";
  const icon = "/icons/icon-192.png";

  const options = {
    body,
    icon,
    badge: icon,
    vibrate: [200, 100, 200],
    tag: payload.data?.taskId || "task-reminder",
    renotify: true,
    data: {
      url: self.location.origin + "/",
      taskId: payload.data?.taskId || "",
    },
    actions: [
      { action: "open", title: "Open App" },
      { action: "dismiss", title: "Dismiss" },
    ],
  };

  return self.registration.showNotification(title, options);
});

self.addEventListener("notificationclick", (event) => {
  event.notification.close();

  if (event.action === "dismiss") {
    return;
  }

  event.waitUntil(
    clients.matchAll({ type: "window", includeUncontrolled: true }).then((clientList) => {
      for (const client of clientList) {
        if ("focus" in client) {
          return client.focus();
        }
      }

      return clients.openWindow("/");
    }),
  );
});
`;

fs.writeFileSync(outputPath, workerSource, "utf8");
console.log(`Generated firebase-messaging-sw.js for mode "${mode}"`);
